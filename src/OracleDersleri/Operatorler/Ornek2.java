package OracleDersleri.Operatorler;

public class Ornek2 {

    public static void main(String[] args) {


        int sonuc = +1;
        // +1


        sonuc--;

        System.out.println(sonuc); // 0

        sonuc++;

        System.out.println(sonuc); // 1

        sonuc = -sonuc;

        System.out.println(sonuc); // -1

        boolean success = false;
        // false
        System.out.println(success);
        // true
        System.out.println(!success);


    }


}
