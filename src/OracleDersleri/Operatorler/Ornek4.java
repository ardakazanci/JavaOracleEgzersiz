package OracleDersleri.Operatorler;

public class Ornek4 {

    /*

        ==  eşit
        !=  eşit değil
        >   daha büyük
        >=  daha büyük veya eşit
        <   daha az
        <=  veya eşittir


     */


    public static void main(String[] args) {


        int deger1 = 1;
        int deger2 = 2;

        if (deger1 == deger2) {

            System.out.println("Değer1 == Değer2");

        }
        if (deger1 != deger2) {

            System.out.println("Değer1 != Değer2");

        }
        if (deger1 > deger2) {

            System.out.println("Değer1 > Değer2");

        }
        if (deger1 < deger2) {

            System.out.println("Değer1 < Değer2");

        }
        if (deger1 <= deger2) {

            System.out.println("Değer1 <= Değer2");

        }


    }


}
