package OracleDersleri.Operatorler;

public class Ornek1 {

    public static void main(String[] args) {

        int toplam, cikarma, carpma, bolme, kalan;
        int sayi1 = 10, sayi2 = 2;


        System.out.println("Toplama İşlemi : " + (toplam = sayi1 + sayi2));
        System.out.println("Çıkarma İşlemi : " + (cikarma = sayi1 - sayi2));
        System.out.println("Çarpma İşlemi : " + (carpma = sayi1 * sayi2));
        System.out.println("Bölme İşlemi : " + (bolme = sayi1 / sayi2));
        System.out.println("Kalan İşlemi : " + (kalan = sayi1 % sayi2));


    }

}
